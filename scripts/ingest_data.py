def sha256_hex(content: str) -> str:
    return hashlib.sha256(content.encode("utf-8")).hexdigest()


def ensure_table(schema: str, table_name: str) -> Table:
    metadata = MetaData(schema=schema)
    table = Table(
        table_name,
        metadata,
        Column("id", BigInteger, primary_key=True, autoincrement=True),
        Column("content", Text, nullable=False),
        Column("content_hash", String(64), nullable=False, unique=True),
        Column("source", Text, nullable=False, server_default=sql_text("'manual'")),
        Column("created_at", TIMESTAMP(timezone=True), server_default=sql_text("now()")),
    )
    metadata.create_all(engine, checkfirst=True)
    return table


def read_text_from_file(path: Optional[str]) -> Optional[str]:
    if not path:
        return None
    p = Path(path).expanduser().resolve()
    return p.read_text(encoding="utf-8")


def ingest(text_content: str, schema: str, table_name: str, source: str) -> Optional[int]:
    content = text_content.strip()
    if not content:
        return None

    table = ensure_table(schema=schema, table_name=table_name)
    h = sha256_hex(content)

    with engine.begin() as conn:
        res = conn.execute(
            insert(table).values(content=content, content_hash=h, source=source)
            .on_conflict_do_nothing(index_elements=[table.c.content_hash])
        )
        # res.rowcount may be -1 for some drivers; ignore and fetch id by hash
        inserted = res.rowcount if res.rowcount is not None else 1
        if inserted == 0:
            # already exists; fetch existing id
            existing = conn.execute(
                sql_text(
                    f"select id from {table.schema}.{table.name} where content_hash = :h limit 1"
                ),
                {"h": h},
            ).scalar_one()
            return int(existing)
        # fetch id of the row we just inserted
        new_id = conn.execute(
            sql_text(
                f"select id from {table.schema}.{table.name} where content_hash = :h limit 1"
            ),
            {"h": h},
        ).scalar_one()
        return int(new_id)


def main():
    parser = argparse.ArgumentParser(description="Ingest pasted conversation text into Postgres (ingest_raw)")
    parser.add_argument("--file", "-f", help="Read content from file instead of RAW_TEXT block")
    parser.add_argument("--schema", "-s", default="public", help="Target schema (default: public)")
    parser.add_argument("--table", "-t", default="ingest_raw", help="Target table name (default: ingest_raw)")
    parser.add_argument("--source", default="manual", help="Source label to store (default: manual)")
    args = parser.parse_args()

    content = read_text_from_file(args.file) or RAW_TEXT
    row_id = ingest(content, schema=args.schema, table_name=args.table, source=args.source)

    if row_id is None:
        print("No content to ingest. Paste text into RAW_TEXT or pass --file.")
    else:
        print(f"Ingested into {args.schema}.{args.table} id={row_id}")


if __name__ == "__main__":
    main()
